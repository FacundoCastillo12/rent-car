const Car = require('../entity/car');

exports.fromModelToEntity = ({
  id,
  brand,
  model,
  year,
  kms,
  colour,
  air,
  passenger,
  img,
  transmission,
  price,
  createdAt,
  updatedAt,
}) => new Car(
  id,
  brand,
  model,
  year,
  kms,
  colour,
  air,
  passenger,
  img,
  transmission,
  Number(price),
  createdAt,
  updatedAt,
);

exports.fromFormToEntity = ({
  id,
  brand,
  model,
  year,
  kms,
  colour,
  air,
  passenger,
  img,
  transmission,
  price,
}) => new Car(
  Number(id),
  brand,
  model,
  Number(year),
  Number(kms),
  colour,
  air,
  Number(passenger),
  img,
  transmission,
  Number(price),
);
