const User = require('../entity/user');

exports.fromModelToEntity = ({
  id,
  firstName,
  lastName,
  docType,
  docNumber,
  nationality,
  address,
  phone,
  email,
  birthdate,
  createdAt,
  updatedAt,
}) => new User(
  Number(id),
  firstName,
  lastName,
  docType,
  Number(docNumber),
  nationality,
  address,
  phone,
  email,
  birthdate,
  createdAt,
  updatedAt,
);

exports.fromFormToEntity = ({
  id,
  'first-name': firstName,
  'last-name': lastName,
  'doc-type': docType,
  'doc-number': docNumber,
  nationality,
  address,
  phone,
  email,
  birthdate,
}) => new User(
  id,
  firstName,
  lastName,
  docType,
  docNumber,
  nationality,
  address,
  phone,
  email,
  birthdate,
);
